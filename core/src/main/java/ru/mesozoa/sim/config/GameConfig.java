package ru.mesozoa.sim.config;

import ru.mesozoa.sim.model.Species;

import java.util.EnumMap;
import java.util.Map;

/**
 * Конфигурация общих параметров игры.
 * - сколько игроков
 * - сколько раундов
 * - ограничения по тайлам
 * - состав колоды заданий штаба
 */
public final class GameConfig {

    /**
     * Количество игроков в партии.
     * Каждый игрок управляет собственной командой рейнджеров:
     * разведчиком, водителем, инженером и охотником.
     */
    public int players = 2;

    /**
     * Максимальное количество раундов партии.
     *
     * В новом очковом режиме партия длится до этого лимита или до окончания
     * основных тайлов исследования. 100 раундов всё ещё много, но хотя бы уже
     * не мезозойская ипотека на 280 раундов.
     */
    public int maxRounds = 100;

    /**
     * Сколько карт задания штаб выдаёт каждому игроку.
     *
     * Игрок получает именно карты, а не уникальные виды: дубли разрешены и дают
     * отдельные бонусные доставки x2 по очкам.
     */
    public int headquartersTaskDinosaurCount = 3;

    /**
     * Сколько копий каждой карты вида лежит в общей колоде заданий штаба.
     *
     * При шести текущих видах и пяти копиях на вид получается колода из 30 карт.
     */
    public int headquartersTaskCardsPerSpecies = 5;

    /**
     * Завершать ли партию, когда закончились основные тайлы исследования.
     *
     * В очковом режиме это второе условие окончания после лимита раундов: остров
     * полностью открыт, дальше игра не должна симулировать вечное ожидание.
     */
    public boolean endWhenMainTileBagIsEmpty = true;

    /**
     * Старый порог завершения по числу выполнивших задание игроков.
     *
     * Оставлен для совместимости старых сценариев, но в новом очковом режиме по
     * умолчанию не используется: игроки продолжают ловить бонусных динозавров.
     */
    public int completedPlayersToEnd = 0;

    /**
     * Включает старое завершение партии по выполненным заданиям.
     *
     * false — новый очковый режим: игра заканчивается по 100 раундам или пустому
     * мешку тайлов. true — старое автоокончание по completedPlayersToEnd.
     */
    public boolean endByCompletedPlayers = false;

    /**
     * Количество спаун-маркеров для каждого вида динозавров.
     * Эти значения определяют, сколько дополнительных тайлов с любимым биомом
     * конкретного вида будет помечено силуэтом динозавра перед началом партии.
     * Новые тайлы при этом не создаются: маркируются уже существующие тайлы
     * дополнительного мешочка.
     *
     * Ключ — вид динозавра.
     * Значение — количество его спаун-тайлов.
     */
    public final Map<Species, Integer> spawnTiles = new EnumMap<>(Species.class);

    public GameConfig() {
        spawnTiles.put(Species.GALLIMIMON, 8);
        spawnTiles.put(Species.DRIORNIS, 8);
        spawnTiles.put(Species.CRYPTOGNATH, 8);
        spawnTiles.put(Species.VELOCITAURUS, 4);
        spawnTiles.put(Species.MONOCERATUS, 4);
        spawnTiles.put(Species.VOCAREZAUROLOPH, 4);
    }
}
