package ru.mesozoa.sim.config;

public class MechanicConfig {

    /**
     * Базовый шанс успешной поимки динозавра через выслеживание.
     * Используется для видов, которые ловятся механикой TRACKING.
     *
     * Например, значение 0.20 означает 20% базового шанса.
     */
    public double trackingBaseSuccess = 0.20;

    /**
     * Прибавка к шансу успешного выслеживания за прогресс/шаг выслеживания.
     * Итоговый шанс считается как базовый шанс плюс случайное количество таких прибавок.
     *
     * Например, при base = 0.20 и stepBonus = 0.20 шанс может стать 20%, 40% или 60%,
     * в зависимости от текущей упрощённой логики симуляции.
     */
    public double trackingStepBonus = 0.20;

    /**
     * Максимальное количество шагов прогресса, которое может учитываться при выслеживании.
     * В текущей симуляции используется как верхняя граница для случайного бонуса
     * к шансу поимки через TRACKING.
     */
    public int trackingMaxSteps = 3;

    /**
     * Базовый шанс успешной поимки динозавра через охоту.
     * Используется для видов, которые ловятся механикой HUNT,
     * когда охотник действует без подготовленного преимущества.
     *
     * Например, значение 0.35 означает 35% шанса.
     */
    public double huntBaseSuccess = 0.35;

    /**
     * Повышенный шанс успешной поимки динозавра через охоту.
     * Используется для подготовленной охоты или ситуации,
     * когда симуляция считает действие охотника более выгодным.
     *
     * Например, значение 0.65 означает 65% шанса.
     */
    public double huntPreparedSuccess = 0.65;
}
